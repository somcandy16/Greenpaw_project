package model1;

public class UserTO {
	private String seq;
	private String mail;
	private String nickname;
	private String password;
	private String authType;
	private String profileUrl;
	private String createdAt;
	private String updatedAt;
	private String hintPassword;
	private String answerPassword;
	private String fromSocial;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getAuthType() {
		return authType;
	}
	public void setAuthType(String authType) {
		this.authType = authType;
	}
	public String getProfileUrl() {
		return profileUrl;
	}
	public void setProfileUrl(String profileUrl) {
		this.profileUrl = profileUrl;
	}
	public String getCreatedAt() {
		return createdAt;
	}
	public void setCreatedAt(String createdAt) {
		this.createdAt = createdAt;
	}
	public String getUpdatedAt() {
		return updatedAt;
	}
	public void setUpdatedAt(String updatedAt) {
		this.updatedAt = updatedAt;
	}
	public String getHintPassword() {
		return hintPassword;
	}
	public void setHintPassword(String hintPassword) {
		this.hintPassword = hintPassword;
	}
	public String getAnswerPassword() {
		return answerPassword;
	}
	public void setAnswerPassword(String answerPassword) {
		this.answerPassword = answerPassword;
	}
	public String getFromSocial() {
		return fromSocial;
	}
	public void setFromSocial(String fromSocial) {
		this.fromSocial = fromSocial;
	}
	
}
